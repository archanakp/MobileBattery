package android.app.mobilebattery.Pojo;

public class VehicleServiceListPojo {

    private String id, vehicle_id, service_id, km_completed, next_service_km, service_date,
            next_service_date, receipt_id, receipt_image, created_at, updated_at;



    public VehicleServiceListPojo() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getVehicle_id() {
        return vehicle_id;
    }

    public void setVehicle_id(String vehicle_id) {
        this.vehicle_id = vehicle_id;
    }

    public String getService_id() {
        return service_id;
    }

    public void setService_id(String service_id) {
        this.service_id = service_id;
    }

    public String getKm_completed() {
        return km_completed;
    }

    public void setKm_completed(String km_completed) {
        this.km_completed = km_completed;
    }

    public String getNext_service_km() {
        return next_service_km;
    }

    public void setNext_service_km(String next_service_km) {
        this.next_service_km = next_service_km;
    }

    public String getService_date() {
        return service_date;
    }

    public void setService_date(String service_date) {
        this.service_date = service_date;
    }

    public String getNext_service_date() {
        return next_service_date;
    }

    public void setNext_service_date(String next_service_date) {
        this.next_service_date = next_service_date;
    }

    public String getReceipt_id() {
        return receipt_id;
    }

    public void setReceipt_id(String receipt_id) {
        this.receipt_id = receipt_id;
    }

    public String getReceipt_image() {
        return receipt_image;
    }

    public void setReceipt_image(String receipt_image) {
        this.receipt_image = receipt_image;
    }

    public String getCreated_at() {
        return created_at;
    }

    public void setCreated_at(String created_at) {
        this.created_at = created_at;
    }

    public String getUpdated_at() {
        return updated_at;
    }

    public void setUpdated_at(String updated_at) {
        this.updated_at = updated_at;
    }
}
